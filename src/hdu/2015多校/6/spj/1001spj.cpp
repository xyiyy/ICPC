int A[MAXN], n;
char op[MAXN];
set<PII> S;

void exit(const char *s, int status) {
    printf("%s\n", s);
    exit(status);
}

bool is_adj(int x, int y) {
    if (x < 1 || x > n) return false;
    if (y < 1 || y > n) return false;
    if (x > y) swap(x, y);
    if (S.count(PII(x, y))) return false;
    S.insert(PII(x, y));
    return x + 1 == y || (x == 1 && y == n);
}

int main(int argc, char **argv) {
    FILE *fin = fopen(argv[1], "r");
    int T;
    fscanf(fin, "%d", &T);
    for (int cas = 0; cas < T; ++cas) {
        fscanf(fin, "%d", &n);
        for (int i = 1; i <= n; ++i) {
            fscanf(fin, "%d", A + i);
        }
        scanf("%s", op);
        S.clear();
        if (strcmp(op, "YES") == 0) {
            //if (ans[cas] == 0) exit("WA", 0);
            int m;
            scanf("%d", &m);
            if (m < 0 || m > n) exit("m exceed range", 0);
            for (int i = 0; i < m; ++i) {
                int u, v;
                scanf("%d%d", &u, &v);
                if (!is_adj(u, v)) exit("not adjacent", 0);
                A[u]--;
                A[v]++;
                if (A[u] < 0) exit("invalid given", 0);
            }
            for (int i = 1; i <= n; ++i) {
                if (A[i] != A[1]) exit("not all the same", 0);
            }
        }
        else if (strcmp(op, "NO") == 0) {
            if (ans[cas] == 1) exit("WA", 0);
        }
        else exit("please output YES or NO", 0);
    }
    exit("AC", 0);
}
